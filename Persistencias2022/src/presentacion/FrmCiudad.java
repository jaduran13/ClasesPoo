    public void mostrarRegistros(){
        DefaultTableModel dtm = new DefaultTableModel();
        
        String titulos[]={"CÃ³digo", "Ciudades"};
        dtm.setColumnIdentifiers(titulos);
        
        Object reg[] = new Object[2];
        
        for(Ciudad c : regCiudad.getListaReg()){
            reg[0]= c.getId();
            reg[1]=c.getNombre();
           
            dtm.addRow(reg);
        }
        TblRegistros.setModel(dtm);
    }
