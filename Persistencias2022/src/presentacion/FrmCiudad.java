    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
        Ciudad c = new Ciudad(
                Integer.parseInt(TfCodigo.getText()),
                TfNombre.getText()
        );
        if(regCiudad.agregarRegistro(c)){
            JOptionPane.showMessageDialog(this, "El registro se guardo satisfactoriamente",
                    "Guardar", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Ocurrio un error al guardar", "Guardar",
                    JOptionPane.WARNING_MESSAGE);
        }
        regCiudad.llenarListado();
        mostrarRegistros();
        TpRegistros.setSelectedIndex(1);
        
    }//GEN-LAST:event_GuardarActionPerformed

